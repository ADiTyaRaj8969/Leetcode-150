"""
Integer to Roman

Convert an integer to Roman numeral string.
"""

def int_to_roman(num: int) -> str:
    vals = [
        (1000,'M'),(900,'CM'),(500,'D'),(400,'CD'),(100,'C'),(90,'XC'),(50,'L'),(40,'XL'),(10,'X'),(9,'IX'),(5,'V'),(4,'IV'),(1,'I')
    ]
    res = []
    for v, s in vals:
        while num >= v:
            res.append(s)
            num -= v
        if num == 0:
            break
    return ''.join(res)


if __name__ == '__main__':
    print('IntToRoman 1:', int_to_roman(3))
    print('IntToRoman 2:', int_to_roman(58))
    print('IntToRoman 3:', int_to_roman(1994))

# Time Complexity: O(1) (bounded)
# Space Complexity: O(1)
